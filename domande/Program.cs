1.	Nella memoria principale (o Memoria centrale) vengono caricati il programma e i dati necessari per eseguirlo e può essere a:
-Breve - medio termine: informazione mantenuta per il tempo dell'esecuzione
-Volatile: informazione persa se la macchina viene spenta.
Esempi di memoria principale sono la RAM e la Cache.
Nella Memoria secondari invece vengono caricati programmi e dati in maniera permanente e può essere a:
-Lungo termine: informazione rimane in maniera permanente
-Non volatile: contenuto della memoria non viene perso con lo spegnimento del computer.
Esempi di memoria secondaria sono Hard Disk, CD, DVD.
La memoria virtuale è un blocco di spazio su disco rigido o SSD che viene utilizzata quando la RAM fisica sta per esaurirsi durante l’esecuzione dei programmi.

2.	Un linguaggio tippizzato garantisce che il valore inserito nella variabile sia utilizzato in maniera coerente rispetto al tipo della variabile. I tipi di base in C# sono:
-	Float: singolo byte
-Double: doppio byte
-Int: memorizza gli interi
-	Char: permette di memorizzare un singolo carattere
-	Decimal: utilizzato per i valori monetari
-	String: memorizza parole o frasi
-	Boolean: true o false

