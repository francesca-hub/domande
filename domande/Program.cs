4.	Gli operatori logici sono AND (&&) e OR(||) che hanno le rispettive tabelle di verità:
-AND: il risultato è sempre FALSO tranne nel caso in cui i due valori siano uguali;
-OR: il risultato è sempre VERO tranne nel caso in cui i due valori siano uguali.

